import Foundation
import UIKit

extension UIColor {
    class func primaryBlue() -> UIColor {
        return UIColor(red: 12/255.0, green: 27/255.0, blue: 50/255.0, alpha: 1.0);
    }
    
    class func secondaryBlue() -> UIColor {
        return UIColor(red: 7/255.0, green: 70/255.0, blue: 101/255.0, alpha: 1.0);
    }
    
    class func primaryPink() -> UIColor {
        return UIColor(red: 240/255.0, green: 100/255.0, blue: 100/255.0, alpha: 1.0);
    }

    class func primaryYellow() -> UIColor {
        return UIColor(red: 252/255.0, green: 234/255.0, blue: 165/255.0, alpha: 1.0);
    }

    class func secondaryYellow() -> UIColor {
        return UIColor(red: 248/255.0, green: 175/255.0, blue: 112/255.0, alpha: 1.0);
    }
}